    <p><strong>¿Necesitas ayuda para hacer tu pastillero?</strong></p>
<p>A continuación se explicará cada apartado del pastillero, los campos no son obligatorios así que podrás ir rellenándolo según lo necesites o lo comprendas mejor.
Esto último es importante porque la finalidad de esta sección es que puedas crear un pastillero virtual que te ayude a la hora de hacer el tuyo en casa.</p>

<p><strong>-Nombre:</strong> aquí vendría el nombre del fármaco.</p>
<p><strong>-Dosis:</strong> generalmente es el número que viene a continuación del nombre del fármaco y es la cantidad de medicamento que se ingiere.</p>
<p><strong>-Descripción:</strong>este apartado se podría utilizar para describir la pastilla o introducir datos a elección del usuario.</p>
<p><strong>-Tomas diarias:</strong> hace referencia a cantas veces se debe incluir el fármaco en el pastillero, explicado de otra forma, el número de veces que debemos tomarnos el medicamento. Podría poner el nº seguido del momento del día.</p>
<p><strong>-Principio activo:</strong> esta información no es de real importancia si no la conoce, a la hora de hacer el pastillero. El principio activo es la sustancia del medicamento que hace efecto, es decir ,que alivia los dolores, que baja la inflamación…</p>
<p><strong>-Forma farmacéutica:</strong> es la forma del medicamento, si es una cápsula, supositorio, pastilla efervescente…</p>
<p><strong>-Vía de administración:</strong> oral, subcutánea, rectal…</p>

